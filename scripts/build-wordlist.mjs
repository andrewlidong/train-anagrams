// Generates src/data/words.ts from a common-English frequency list.
// Run with: npm run build:wordlist
import { writeFile } from "node:fs/promises";

const SOURCE =
  "https://raw.githubusercontent.com/first20hours/google-10000-english/master/google-10000-english-no-swears.txt";

const res = await fetch(SOURCE);
if (!res.ok) throw new Error(`Failed to fetch word list: ${res.status}`);
const text = await res.text();

const words = [
  ...new Set(
    text
      .split(/\r?\n/)
      .map((w) => w.trim().toLowerCase())
      .filter((w) => /^[a-z]{2,}$/.test(w)),
  ),
];

const header = `// AUTO-GENERATED by scripts/build-wordlist.mjs — do not edit by hand.
// Source: google-10000-english (most common English words).
// ${words.length} words.
`;
const body = `export const WORDS: string[] = ${JSON.stringify(words)};\n`;

await writeFile(new URL("../src/data/words.ts", import.meta.url), header + body);
console.log(`Wrote ${words.length} words to src/data/words.ts`);
